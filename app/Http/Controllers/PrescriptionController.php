    {
        $prescription = Prescription::find($id);
        // dd($prescription->prescriptioneetail);
        $total = 0;
        foreach ($prescription->prescriptioneetail as $key => $value) {
          $total += $value->drug->price * $value->quantity;
        }
        return view('prescription.show')->withPrescription($prescription)->withTotal($total);
    }

